            public void searchCustomer(String text) {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `customer` WHERE `id` LIKE '"+text+"' OR `name` LIKE '"+text+"%' OR `Contact_number` LIKE '"+text+"%'");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("Contact_number"));

                dtm.addRow(v);
            }
            jTable1.setModel(dtm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
