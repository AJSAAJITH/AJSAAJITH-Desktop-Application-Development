    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String price = jTextField2.getText();
        String type = jComboBox1.getSelectedItem().toString();
        
        if (price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please add New Salary Fee", "Warrning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("([1-9][0-9]*)|(([1-9][0-9]*[.]([0]*[1-9][0-9]*)))|([0][.]([0]*[1-9][0-9]*))").matcher(price).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Salary Value", "Warrning", JOptionPane.WARNING_MESSAGE);
        } else if (type.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select User Position", "Warrning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `company_user_type` WHERE `name`='" + type + "'");
                rs.next();
                String type_id = rs.getString("id");
                MySQL.iud("UPDATE `salary` SET `Price`='" + price + "' WHERE `position`='" + type_id + "'");
                JOptionPane.showMessageDialog(this, "Position Salary Fee Update Sucessfully", "Sucess", JOptionPane.INFORMATION_MESSAGE);
                jTextField2.setText("");
                jComboBox1.setSelectedItem("Select");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

