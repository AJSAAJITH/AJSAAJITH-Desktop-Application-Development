    public void loadtable() {
        try {
            ResultSet rs = MySQL.search("SELECT `company_user`.`id`,`company_user`.`name`,`company_user`.`NIC`,`company_user_type`.`name`,`company_user_payment`.`salary`,`company_user_payment`.`out_date` FROM `company_user_payment` INNER JOIN `company_user` ON `company_user`.`id` =`company_user_payment`.`user_id` INNER JOIN `company_user_type` ON `company_user_type`.`id` = `company_user`.`company_user_type_id`");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {                
                Vector v = new Vector();
                v.add(rs.getString("company_user.id"));
                v.add(rs.getString("company_user.name"));
                v.add(rs.getString("company_user.NIC"));
                v.add(rs.getString("company_user_type.name"));
                v.add(rs.getString("company_user_payment.salary"));
                v.add(rs.getString("company_user_payment.out_date"));
                
                dtm.addRow(v);
            }
            jTable1.setModel(dtm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
