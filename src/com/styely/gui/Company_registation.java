    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String cname = jTextField1.getText();
        String cnumber = jTextField2.getText();

        if (cname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Company name", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (cnumber.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Company Hotline number", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (Pattern.compile("01[01245678][0-9]{7}").matcher(cname).matches()) {

            JOptionPane.showMessageDialog(this, "Please Enter company Hotline number nit acsept private numbers", "warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `supplier_company` WHERE `name`='" + cname + "' AND `company_number`='" + cnumber + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This Company already exsist in the System", "warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.iud("INSERT INTO `supplier_company`(`name`,`company_number`) VALUES('" + cname + "','" + cnumber + "')");
                    JOptionPane.showMessageDialog(this, "New Company Adding Sucess", "Sucess", JOptionPane.INFORMATION_MESSAGE);
                    loadCompany();
                    reset();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTextField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyTyped
        // TODO add your handling code here:
        String mobile = jTextField2.getText();
        String text = mobile + evt.getKeyChar();

        if (text.length() == 1) {

            if (!text.equals("0")) {
                evt.consume();
            }
        } else if (text.length() == 2) {

            if (!text.equals("01")) {
                evt.consume();
            }
        } else if (text.length() == 3) {

            if (!Pattern.compile("01[01245678]").matcher(text).matches()) {
                evt.consume();
            }
        } else if (text.length() <= 10) {

            if (!Pattern.compile("01[01245678][0-9]+").matcher(text).matches()) {
                evt.consume();
            }
        } else {

            evt.consume();
        }
    }//GEN-LAST:event_jTextField2KeyTyped

