    public void loadCategory() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `catecory`");
            Vector v = new Vector();
            v.add("Select");
            while (rs.next()) {

                v.add(rs.getString("name"));
            }
            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
        public void loadsection() {
        try {
            ResultSet rs = MySQL.search("SELECT `sub_category`.`id`,`sub_category`.`name`,`catecory`.`name` FROM `sub_category` INNER JOIN `catecory` ON `catecory`.`id` = `sub_category`.`Catecory_id`");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("sub_category.id"));
                v.add(rs.getString("sub_category.name"));
                v.add(rs.getString("catecory.name"));

                dtm.addRow(v);
            }
            jTable1.setModel(dtm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

