    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String category = jComboBox1.getSelectedItem().toString();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter section name", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (category.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select aCategory", "warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `catecory` WHERE `name`='" + category + "'");
                rs.next();
                String category_id = rs.getString("id");
                ResultSet rs1 = MySQL.search("SELECT * FROM `sub_category` WHERE `name`='" + name + "' AND `Catecory_id`='" + category_id + "'");
                if (rs1.next()) {
                    JOptionPane.showMessageDialog(this, "This Section is Already exsist.", "warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.iud("INSERT INTO `sub_category`(`name`,`Catecory_id`) VALUES('" + name + "','" + category_id + "')");
                    JOptionPane.showMessageDialog(this, "New Section Adding Sucessfull", "Sucess", JOptionPane.INFORMATION_MESSAGE);
                    loadsection();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

