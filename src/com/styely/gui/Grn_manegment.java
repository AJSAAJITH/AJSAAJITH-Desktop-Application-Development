    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String s_id = jLabel3.getText();
        String p_id = jLabel9.getText();
        String qty = jTextField1.getText();
        String b_price = jTextField2.getText();
        String s_price = jTextField3.getText();
        String size = jComboBox1.getSelectedItem().toString();

        if (s_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select a supplier", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (p_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select a Product", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (qty.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Quentity", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (b_price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Buying price", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("([1-9][0-9]*)|(([1-9][0-9]*[.]([0]*[1-9][0-9]*)))|([0][.]([0]*[1-9][0-9]*))").matcher(b_price).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid buying price", "Waning", JOptionPane.WARNING_MESSAGE);
        } else if (s_price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Selling price", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("([1-9][0-9]*)|(([1-9][0-9]*[.]([0]*[1-9][0-9]*)))|([0][.]([0]*[1-9][0-9]*))").matcher(s_price).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Selling price", "Waning", JOptionPane.WARNING_MESSAGE);
        } else if (size.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select a Size", "warning", JOptionPane.WARNING_MESSAGE);
        } else {

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            boolean isFound = false;

            int x = -1;

            for (int i = 0; i < jTable1.getRowCount(); i++) {
                String id = jTable1.getValueAt(i, 0).toString();
                String p_size = jTable1.getValueAt(i, 6).toString();

                if (id.equals(p_id)) {
                    if (p_size.equals(size)) {

                        isFound = true;
                        x = i;
                        break;
                    }
                }
            }

            if (isFound) {
                int option = JOptionPane.showConfirmDialog(this, "This product is Allreay add to the grm. Do you want to Update this", "INformation", JOptionPane.INFORMATION_MESSAGE, JOptionPane.YES_NO_OPTION);
                //Update 
                if (option == JOptionPane.YES_OPTION) {

                    int oldQty = Integer.parseInt(jTable1.getValueAt(x, 5).toString());
                    int finalQty = oldQty + Integer.parseInt(qty);
                    jTable1.setValueAt(String.valueOf(finalQty), x, 5);
                    jTable1.setValueAt(b_price, x, 7);

                    double UpdateintemTotal = finalQty * Double.parseDouble(b_price);
                    jTable1.setValueAt(String.valueOf(UpdateintemTotal), x, 9);

                    //total payment update
                    updateTotal();
                } else {
                }

                reset();

            } else {
                Vector v = new Vector();
                v.add(p_id);
                v.add(jLabel11.getText());
                v.add(jLabel13.getText());
                v.add(jLabel15.getText());
                v.add(jLabel17.getText());
                v.add(qty);
                v.add(size);
                v.add(b_price);
                v.add(s_price);

                double itemtotal = Integer.parseInt(qty) * Double.parseDouble(b_price);
                v.add(df.format(itemtotal));

                dtm.addRow(v);

                updateTotal();
                reset();

                if (jTable1.getRowCount() == 1) {
                    jButton1.setText("Update Supplier");
                    jButton1.setEnabled(false);
                }
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

